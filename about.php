<p style="font-size:120%;  color:white; font-family:Lucida Console">Hetauda Cement Industries(HCI), established in BS 2033 Ashwin 13 under Company Act 2021, is an undertaking of HMG/Nepal. The plant has an annual production capacity of 260000 MT of Cement and was installed with a loan assistance from Asian Development Bank (ADB). It commenced its commercial production since December 1985.
The economic life of cement machinery and mobile quarry equipment is 18 and 5 years respectivetly. As cement production started in fiscal year 2042/43, almost all machines require rehabilitation, which needs huge capital. Therefore, HCI has planned to do rehabilitation work in various fiscal years. In the current fiscal year part of the burning system and quarry equipment will be rehabilitated. Additionally, a study will be conducted for rehabilitation of ESP Great Cooler and cyclone.<br><br>

